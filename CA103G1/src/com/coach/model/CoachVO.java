package com.coach.model;

public class CoachVO {
	
	private String coa_id;
	private String mem_id;
	private String coa_text;
	private String coa_status;
	private Integer coa_recount;
	
	public String getCoa_id() {
		return coa_id;
	}
	public void setCoa_id(String coa_id) {
		this.coa_id = coa_id;
	}
	public String getMem_id() {
		return mem_id;
	}
	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}
	public String getCoa_text() {
		return coa_text;
	}
	public void setCoa_text(String coa_text) {
		this.coa_text = coa_text;
	}
	public String getCoa_status() {
		return coa_status;
	}
	public void setCoa_status(String coa_status) {
		this.coa_status = coa_status;
	}
	public Integer getCoa_recount() {
		return coa_recount;
	}
	public void setCoa_recount(Integer coa_recount) {
		this.coa_recount = coa_recount;
	}


}
